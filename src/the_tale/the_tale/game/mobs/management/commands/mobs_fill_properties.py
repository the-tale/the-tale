class Command(BaseCommand):

    help = 'fill new mobs properties'

    def handle(self,  *args,  **options):

        mobs = {mob['id']: mob for mob in MOBS}

        for mob in models.MobRecord.objects.all().order_by('id').iterator():
            print('process mob {}'.format(mob.id))

            if mob.id not in mobs:
                continue

            new_data = mobs[mob.id]

            data = s11n.from_json(mob.data)

            data['structure'] = new_data['structure'].value
            data['features'] = [feature.value for feature in new_data['features']]
            data['movement'] = new_data['movement'].value
            data['body'] = new_data['body'].value
            data['size'] = new_data['size'].value
            data['weapons'] = [{'weapon': new_data['weapon'].value,
                                'material': MATERIALS[new_data['weapon']].value,
                                'power_type': 2}]

            mob.data = s11n.to_json(data)

            mob.save()
